package com.crowd.streetbuzz.json;

/**
 * @author Atrijit
 *
 */
public class JsonWordCloud {
private String word;
private int count;
private String type;

public int getCount() {
	return count;
}
public void setCount(int count) {
	this.count = count;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public String getWord() {
	return word;
}
public void setWord(String word) {
	this.word = word;
}

}
